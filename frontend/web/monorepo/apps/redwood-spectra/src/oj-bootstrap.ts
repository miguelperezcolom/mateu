/**
 * Boots the Redwood Spectra renderer.
 *
 * The Oracle JET / Spectra web components (oj-c-*, oj-sp-*) are AMD modules served from Oracle's
 * public CDN as BUNDLES (a require() for e.g. `ojs/ojbootstrap` resolves to the bundle file that
 * contains it). We load Oracle's own RequireJS bundle configs FROM the CDN — never copied here —
 * so the require paths + bundle map are always Oracle's authoritative ones. Then Vite serves our
 * own code as ES modules; this file bridges the two:
 *
 *   1. load RequireJS from the CDN,
 *   2. load the JET bundle config (configures ojs/* and oj-c/*) and the Spectra bundle config
 *      (configures oj-sp/*) — both self-execute requirejs.config against the CDN,
 *   3. add the missing `oj-sp` path (the Spectra config only ships the bundle map, no paths),
 *   4. require OJET's base plus EVERY oj-c/oj-sp module the renderer uses, so all custom
 *      elements are defined before the first Lit render; the ArrayDataProvider class and the
 *      ready flag are stashed on window.__mateuOj for the renderer modules,
 *   5. import our ES-module entry (registers <mateu-ui> + the Redwood renderer),
 *   6. adopt the routed <mateu-ui> into #ui-container (or create one from the URL path when the
 *      page was not generated by the AP controllers) and reveal it.
 *
 * If the CDN can't be reached the app still boots: window.__mateuOj.ready stays false and the
 * renderer delegates everything to the shared base renderer (graceful degradation).
 */

// Oracle CDN coordinates — the exact versions the Spectra Shell uses (bump together to move JET /
// Spectra). NOTE the JET version carries the build suffix (…-2604.8): the bundle files only exist
// under that path.
const JET_BASE = 'https://static.oracle.com/cdn/jet/19.0.0-2604.8'
const REQUIRE_JS = `${JET_BASE}/3rdparty/require/require.js`
const JET_BUNDLE_CONFIG = `${JET_BASE}/default/js/bundles-config.js`
const SPECTRA_VERSION = '2604.1.0'
const SPECTRA_BUNDLE_CONFIG = `https://static.oracle.com/cdn/spectra-ui/oj-sp/${SPECTRA_VERSION}/oj-sp-bundle-config.js`

// Every AMD module the renderer needs. Loading them all up-front means the custom elements are
// defined before <mateu-ui> mounts, and no component ever pops in late.
//  - ojs/ojknockout: the oj-sp pack components are Knockout-era composites whose templates use
//    oj-bind-text / oj-bind-if — without this module their bindings never resolve.
//  - ojs/ojarraydataprovider: exports the ArrayDataProvider CLASS as the module value itself
//    (ojs/ojdataprovider in JET 19 only carries operators/utils).
