declare global {
  interface Window {
    require?: any
    requirejs?: any
    __mateuOj?: {
      ready: boolean
      ArrayDataProvider?: any
      require?: any
    }
  }
}

const loadScript = (src: string): Promise<void> =>
  new Promise((resolve, reject) => {
    const s = document.createElement('script')
    s.src = src
    s.async = false
    s.onload = () => resolve()
    s.onerror = () => reject(new Error('failed to load ' + src))
    document.head.appendChild(s)
  })

/** Adopt the routed <mateu-ui> (injected by the AP controllers into the inert template) into the
 * live #ui-container; when the page was NOT generated by the controllers (plain dev server),
 * create a <mateu-ui> that resolves the route from the URL path. Then reveal it. */
async function mountLitRoot() {
  await import('@/index')
  const container = document.getElementById('ui-container')
  const holder = document.getElementById('mateu-ui-holder') as HTMLTemplateElement | null
  if (container && holder && holder.content.childElementCount > 0) {
    container.appendChild(holder.content.cloneNode(true))
  } else if (container && !container.querySelector('mateu-ui')) {
    const ui = document.createElement('mateu-ui')
    ui.setAttribute('baseUrl', '')
    ui.setAttribute('urlPrefix', '')
    container.appendChild(ui)
  }
  if (container) container.style.display = ''
  document.getElementById('landing-loader')?.remove()
}

async function loadOjet() {
  await loadScript(REQUIRE_JS)
  // Oracle's own require configs (paths + bundle map). They self-execute requirejs.config against
  // absolute CDN URLs.
  await loadScript(JET_BUNDLE_CONFIG)
  await loadScript(SPECTRA_BUNDLE_CONFIG)

  const requirejs = window.requirejs
  const require = window.require
  if (!require || !requirejs) throw new Error('RequireJS did not initialise')

  // The Spectra bundle config only registers the bundle map; it never defines the `oj-sp` path,
  // so any oj-sp module would 404 without this (verified against the CDN config).
  requirejs.config({
    paths: {
      'oj-sp': `https://static.oracle.com/cdn/spectra-ui/oj-sp/${SPECTRA_VERSION}`,
    },
  })

  await new Promise<void>((resolve, reject) => {
    require(OJ_MODULES, (...args: unknown[]) => {
      // ojs/ojarraydataprovider is the 3rd module and exports the class as the module value
      const ArrayDataProvider = args[2]
      window.__mateuOj = {
        ready: true,
        ArrayDataProvider,
        require,
      }
      resolve()
    }, (err: unknown) => reject(err))
  })
}

async function boot() {
  window.__mateuOj = { ready: false }
  try {
    await loadOjet()
  } catch (e) {
    // eslint-disable-next-line no-console
    console.warn('[redwood-spectra] OJET CDN not available — falling back to the base renderer.', e)
  }
  await mountLitRoot()
}

boot()
